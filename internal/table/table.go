// Styles contains style definitions for this list component. By default, these
// values are generated by DefaultStyles.
type Styles struct {
	Header   lipgloss.Style
	Cell     lipgloss.Style
	Selected lipgloss.Style

	// ScrollTrack and ScrollThumb style the vertical scrollbar drawn down the
	// right edge when there are more rows than fit. Zero values render an
	// uncolored bar, which is still legible; ui.TableStyles themes them.
	ScrollTrack lipgloss.Style
	ScrollThumb lipgloss.Style
}

// DefaultStyles returns a set of default style definitions for this table.
func DefaultStyles() Styles {
	return Styles{
		Selected: lipgloss.NewStyle().Bold(true).Foreground(lipgloss.Color("212")),
		Header:   lipgloss.NewStyle().Bold(true).Padding(0, 1),
		Cell:     lipgloss.NewStyle().Padding(0, 1),
	}
}

// SetStyles sets the table styles.
func (m *Model) SetStyles(s Styles) {
	m.styles = s
	m.refreshSelStyle()
	m.UpdateViewport()
}

// refreshSelStyle rebuilds the cached selected-row cell style: Cell's padding
// with Selected's colors/bold, so the highlight background covers each cell's
// full width (including padding) rather than being broken by per-cell ANSI
// resets when Cell has its own foreground color set.
func (m *Model) refreshSelStyle() {
	m.selCellStyle = m.styles.Cell.
		Foreground(m.styles.Selected.GetForeground()).
		Background(m.styles.Selected.GetBackground()).
		Bold(m.styles.Selected.GetBold())
}

// Option is used to set options in New. For example:
//
//	table := New(WithColumns([]Column{{Title: "ID", Width: 10}}))
type Option func(*Model)

// New creates a new model for the table widget.
func New(opts ...Option) Model {
	m := Model{
		cursor:   0,
		viewport: viewport.New(0, 20), //nolint:mnd

		frozenCols: 1, // pin the leading column (e.g. the row-number "#") by default

		KeyMap: DefaultKeyMap(),
		Help:   help.New(),
		styles: DefaultStyles(),
	}

	for _, opt := range opts {
		opt(&m)
	}

	m.refreshSelStyle()
	m.fitColumns()
	m.UpdateViewport()

	return m
}

// fitColumns grows every visible column (one with a configured width > 0) so it
// is at least as wide as its widest cell and its header. Columns never shrink
// below their configured width (the floor captured in baseWidths), and columns
// configured with width 0 stay hidden. Because cells are sized to fit, the
// per-cell truncation in renderRow never elides content; tables that overflow
// the view scroll horizontally by whole columns instead (see ScrollRight).
func (m *Model) fitColumns() {
	if len(m.baseWidths) != len(m.cols) {
		m.baseWidths = make([]int, len(m.cols))
		for i, c := range m.cols {
			m.baseWidths[i] = c.Width
		}
	}

	for i := range m.cols {
		base := m.baseWidths[i]
		if base <= 0 {
			// Width-0 columns are intentionally hidden from the table.
			m.cols[i].Width = base
			continue
		}
		w := max(base, runewidth.StringWidth(m.cols[i].Title))
		for _, row := range m.rows {
			if i < len(row) {
				if cw := runewidth.StringWidth(row[i]); cw > w {
					w = cw
				}
			}
		}
		m.cols[i].Width = w
	}

	// Widths are final for this column/row set: rebuild the per-column style
	// cache used by renderRow and headersView.
	m.colStyles = make([]lipgloss.Style, len(m.cols))
	for i, c := range m.cols {
		m.colStyles[i] = lipgloss.NewStyle().Width(c.Width).MaxWidth(c.Width).Inline(true)
	}
}

// colStyle returns the cached per-column style, building one on the fly only
// when the cache isn't populated yet (e.g. option callbacks during New).
func (m Model) colStyle(i int) lipgloss.Style {
	if i < len(m.colStyles) {
		return m.colStyles[i]
	}
	return lipgloss.NewStyle().Width(m.cols[i].Width).MaxWidth(m.cols[i].Width).Inline(true)
}

// SortAscArrow and SortDescArrow are the header suffixes marking the active
// sort column and its direction. Each is two display cells wide (a separating
// space plus the arrow) so the two directions are interchangeable without
// changing a header's width.
const (
	SortAscArrow  = " ↑"
	SortDescArrow = " ↓"
)

// ApplySortHeader marks the active sort column's header with a direction arrow
// (SortAscArrow / SortDescArrow) and reserves room for that arrow in every
// sortable column's width. Because the width is reserved whether or not the
// column currently shows the arrow, the arrow appearing — or moving between
// columns — never changes a column's width, so the table does not reflow
// ("flicker"). Inactive sortable headers stay arrow-free for a clean look.
//
// active is the index of the current sort column, or -1 for none. sortable
// reports whether column i participates in sorting, so non-sortable columns
// (e.g. a positional "#") are left untouched. Call it each render with freshly
// built, arrow-free columns.
func ApplySortHeader(cols []Column, active int, asc bool, sortable func(i int) bool) {
	arrow := SortAscArrow
	if !asc {
		arrow = SortDescArrow
	}
	arrowW := runewidth.StringWidth(arrow)
	for i := range cols {
		if cols[i].Width == 0 || !sortable(i) {
			continue // width-0 columns are hidden; non-sortable ones never sort
		}
		if reserved := runewidth.StringWidth(cols[i].Title) + arrowW; cols[i].Width < reserved {
			cols[i].Width = reserved
		}
		if i == active {
			cols[i].Title += arrow
		}
	}
}

