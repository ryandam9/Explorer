// Model defines a state for the table widget.
type Model struct {
	KeyMap KeyMap
	Help   help.Model

	// MarkRow, when set, wraps each rendered row just before it enters the
	// viewport. Callers use it to register a mouse hit-zone per visible row
	// (e.g. with bubblezone) so rows can be selected by clicking.
	MarkRow func(index int, rendered string) string

	cols   []Column
	rows   []Row
	cursor int
	focus  bool
	styles Styles

	// Horizontal column scrolling. width is the available render width (0 means
	// unconstrained / show every column). frozenCols leading columns are pinned
	// and always shown; colOffset counts how many of the remaining (scrollable)
	// columns are hidden off the left edge.
	width      int
	colOffset  int
	frozenCols int

	// baseWidths holds each column's configured width, captured before any
	// content-based auto-fit. It is the floor a column may shrink to; columns
	// grow past it to fit their widest cell so values are never truncated.
	baseWidths []int

	// colStyles caches the per-column Width/MaxWidth/Inline cell style,
	// rebuilt when column widths change (fitColumns) instead of being
	// re-allocated for every cell on every frame.
	colStyles []lipgloss.Style
	// selCellStyle caches the selected-row cell style derived from styles,
	// rebuilt in SetStyles instead of per render.
	selCellStyle lipgloss.Style

	viewport viewport.Model
	start    int
	end      int
}

// Row represents one line in the table.
type Row []string

// Column defines the table structure.
type Column struct {
	Title string
	Width int
}

// KeyMap defines keybindings. It satisfies to the help.KeyMap interface, which
// is used to render the help menu.
type KeyMap struct {
	LineUp       key.Binding
	LineDown     key.Binding
	PageUp       key.Binding
	PageDown     key.Binding
	HalfPageUp   key.Binding
	HalfPageDown key.Binding
	GotoTop      key.Binding
	GotoBottom   key.Binding
}

// ShortHelp implements the KeyMap interface.
func (km KeyMap) ShortHelp() []key.Binding {
	return []key.Binding{km.LineUp, km.LineDown}
}

// FullHelp implements the KeyMap interface.
func (km KeyMap) FullHelp() [][]key.Binding {
	return [][]key.Binding{
		{km.LineUp, km.LineDown, km.GotoTop, km.GotoBottom},
		{km.PageUp, km.PageDown, km.HalfPageUp, km.HalfPageDown},
	}
}

