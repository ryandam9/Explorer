// Update is the Bubble Tea update loop.
func (m Model) Update(msg tea.Msg) (Model, tea.Cmd) {
	if !m.focus {
		return m, nil
	}

	switch msg := msg.(type) {
	case tea.KeyMsg:
		switch {
		case key.Matches(msg, m.KeyMap.LineUp):
			m.MoveUp(1)
		case key.Matches(msg, m.KeyMap.LineDown):
			m.MoveDown(1)
		case key.Matches(msg, m.KeyMap.PageUp):
			m.MoveUp(m.viewport.Height)
		case key.Matches(msg, m.KeyMap.PageDown):
			m.MoveDown(m.viewport.Height)
		case key.Matches(msg, m.KeyMap.HalfPageUp):
			m.MoveUp(m.viewport.Height / 2) //nolint:mnd
		case key.Matches(msg, m.KeyMap.HalfPageDown):
			m.MoveDown(m.viewport.Height / 2) //nolint:mnd
		case key.Matches(msg, m.KeyMap.GotoTop):
			m.GotoTop()
		case key.Matches(msg, m.KeyMap.GotoBottom):
			m.GotoBottom()
		}
	}

	return m, nil
}

// Focused returns the focus state of the table.
func (m Model) Focused() bool {
	return m.focus
}

// Focus focuses the table, allowing the user to move around the rows and
// interact.
func (m *Model) Focus() {
	m.focus = true
	m.UpdateViewport()
}

// Blur blurs the table, preventing selection or movement.
func (m *Model) Blur() {
	m.focus = false
	m.UpdateViewport()
}

// vScrollGutter is the width reserved on the right for the vertical scrollbar
// (one spacer column plus the bar) when the table has more rows than fit.
const vScrollGutter = 2

// vScrollActive reports whether a vertical scrollbar is shown: the table has a
// constrained width and more rows than the row viewport can display at once.
func (m Model) vScrollActive() bool {
	return m.width > 0 && m.viewport.Height > 0 && len(m.rows) > m.viewport.Height
}

// contentWidth is the width available to columns: the render width minus the
// scrollbar gutter when one is shown, so the bar never pushes the table past
// the width the caller sized it to.
func (m Model) contentWidth() int {
	if m.vScrollActive() {
		return m.width - vScrollGutter
	}
	return m.width
}

// View renders the component.
func (m Model) View() string {
	header := m.headersView()
	rows := m.viewport.View()
	if !m.vScrollActive() {
		return header + "\n" + rows
	}

	// Pair the row area with a vertical scrollbar so a full page no longer hides
	// the fact that there are more rows above/below (issue #155). The bar tracks
	// the topmost visible row within the full row set.
	cw := m.contentWidth()
	top := m.start + m.viewport.YOffset
	bar := RenderVScrollbar(m.viewport.Height, len(m.rows), m.viewport.Height, top,
		m.styles.ScrollTrack, m.styles.ScrollThumb)
	barLines := strings.Split(bar, "\n")

	// Pad each row line to the content width so the bar lands flush at the right
	// edge regardless of how short the widest row is. MaxWidth trims the trailing
	// padding the viewport may add, keeping every line exactly cw + gutter wide.
	pad := lipgloss.NewStyle().Width(cw).MaxWidth(cw)
	rowLines := strings.Split(rows, "\n")
	for i := range rowLines {
		line := pad.Render(rowLines[i])
		if i < len(barLines) {
			line += " " + barLines[i]
		}
		rowLines[i] = line
	}
	return header + "\n" + strings.Join(rowLines, "\n")
}

// RenderVScrollbar renders a one-column vertical scrollbar of the given height:
// a thumb sized to the visible fraction of the content, positioned by the
// scroll offset. When everything fits (total <= visible) it returns a blank
// gutter of spaces so callers can reserve the column unconditionally. track and
// thumb style the respective cells. It lives here (rather than in internal/ui)
// so the table can draw its own scrollbar without importing ui, which imports
// table; ui.VScrollbar delegates to it.
//
// total is the content's line count, visible the number of lines on screen and
// offset the index of the topmost visible line.
func RenderVScrollbar(height, total, visible, offset int, track, thumb lipgloss.Style) string {
	if height < 1 {
		return ""
	}
	blank := strings.TrimRight(strings.Repeat(" \n", height), "\n")
	if total <= visible || visible < 1 {
		return blank
	}

	thumbH := height * visible / total
	if thumbH < 1 {
		thumbH = 1
	}
	if thumbH > height {
		thumbH = height
	}

	maxOffset := total - visible
	travel := height - thumbH
	pos := 0
	if maxOffset > 0 {
		pos = travel * offset / maxOffset
	}
	if pos > travel {
		pos = travel
	}
	if pos < 0 {
		pos = 0
	}

	var b strings.Builder
	for i := 0; i < height; i++ {
		if i > 0 {
			b.WriteByte('\n')
		}
		if i >= pos && i < pos+thumbH {
			b.WriteString(thumb.Render("┃"))
		} else {
			b.WriteString(track.Render("│"))
		}
	}
	return b.String()
}

// HelpView is a helper method for rendering the help menu from the keymap.
// Note that this view is not rendered by default and you must call it
// manually in your application, where applicable.
func (m Model) HelpView() string {
	return m.Help.View(m.KeyMap)
}

// UpdateViewport updates the list content based on the previously defined
// columns and rows.
func (m *Model) UpdateViewport() {
	renderedRows := make([]string, 0, len(m.rows))

	// Render only rows from: m.cursor-m.viewport.Height to: m.cursor+m.viewport.Height
	// Constant runtime, independent of number of rows in a table.
	// Limits the number of renderedRows to a maximum of 2*m.viewport.Height
	if m.cursor >= 0 {
		m.start = clamp(m.cursor-m.viewport.Height, 0, m.cursor)
	} else {
		m.start = 0
	}
	m.end = clamp(m.cursor+m.viewport.Height, m.cursor, len(m.rows))
	vis := m.visibleCols() // resolved once per refresh, not once per row
	for i := m.start; i < m.end; i++ {
		row := m.renderRow(i, vis)
		if m.MarkRow != nil {
			row = m.MarkRow(i, row)
		}
		renderedRows = append(renderedRows, row)
	}

	m.viewport.SetContent(
		lipgloss.JoinVertical(lipgloss.Left, renderedRows...),
	)
}

// VisibleRange returns the half-open [start, end) row index range currently
// rendered into the viewport — the only rows that can have a mouse hit-zone.
func (m Model) VisibleRange() (start, end int) {
	return m.start, m.end
}

// SelectedRow returns the selected row.
// You can cast it to your own implementation.
func (m Model) SelectedRow() Row {
	if m.cursor < 0 || m.cursor >= len(m.rows) {
		return nil
	}

	return m.rows[m.cursor]
}

// Rows returns the current rows.
func (m Model) Rows() []Row {
	return m.rows
}

// Columns returns the current columns.
func (m Model) Columns() []Column {
	return m.cols
}

// SetRows sets a new rows state.
func (m *Model) SetRows(r []Row) {
	m.rows = r

	if m.cursor > len(m.rows)-1 {
		m.cursor = len(m.rows) - 1
	}
	// Recover from an empty row set (cursor -1) once rows arrive, so the
	// first row is selected without requiring a cursor movement first.
	if m.cursor < 0 && len(m.rows) > 0 {
		m.cursor = 0
	}

	m.fitColumns()
	if max := m.maxColOffset(); m.colOffset > max {
		m.colOffset = max
	}
	m.UpdateViewport()
}

// SetColumns sets a new columns state.
func (m *Model) SetColumns(c []Column) {
	m.cols = c
	m.baseWidths = nil // recapture configured widths for the new column set
	m.colOffset = 0    // new column set: start scrolled fully left
	m.fitColumns()
	m.UpdateViewport()
}

// SetWidth sets the render width available to the table. Columns that do not
// fit are scrolled rather than truncated; see ScrollLeft / ScrollRight.
func (m *Model) SetWidth(w int) {
	m.width = w
	m.viewport.Width = w
	if max := m.maxColOffset(); m.colOffset > max {
		m.colOffset = max
	}
	m.UpdateViewport()
}

// ScrollRight reveals the next hidden column on the right, if any.
func (m *Model) ScrollRight() {
	if m.colOffset < m.maxColOffset() {
		m.colOffset++
		m.UpdateViewport()
	}
}

// ScrollLeft reveals the previous hidden column on the left, if any.
func (m *Model) ScrollLeft() {
	if m.colOffset > 0 {
		m.colOffset--
		m.UpdateViewport()
	}
}

// ColScrollInfo reports how many scrollable (non-frozen) columns are currently
// hidden off the left and right edges. Both zero means every column is visible.
func (m Model) ColScrollInfo() (hiddenLeft, hiddenRight int) {
	pos, frozen := m.layoutCols()
	scroll := pos[frozen:]
	if m.contentWidth() <= 0 || len(scroll) == 0 {
		return 0, 0
	}
	hiddenLeft = m.colOffset
	if hiddenLeft > len(scroll) {
		hiddenLeft = len(scroll)
	}
	visScroll := len(m.visibleCols()) - frozen
	hiddenRight = len(scroll) - hiddenLeft - visScroll
	if hiddenRight < 0 {
		hiddenRight = 0
	}
	return hiddenLeft, hiddenRight
}

// layoutCols returns the indexes of columns with a positive width (the ones
// that actually render) and the effective number of frozen leading columns.
func (m Model) layoutCols() (pos []int, frozen int) {
	for i, c := range m.cols {
		if c.Width > 0 {
			pos = append(pos, i)
		}
	}
	frozen = m.frozenCols
	if frozen > len(pos) {
		frozen = len(pos)
	}
	return pos, frozen
}

// colSpan is the on-screen width a column occupies, including cell padding.
func (m Model) colSpan(i int) int {
	return m.cols[i].Width + m.styles.Cell.GetHorizontalPadding()
}

// visibleCols returns the column indexes to render given the current width and
// scroll offset: the frozen columns followed by as many scrollable columns
// (starting at colOffset) as fit.
func (m Model) visibleCols() []int {
	pos, frozen := m.layoutCols()
	cw := m.contentWidth()
	if cw <= 0 {
		return pos
	}
	res := append([]int{}, pos[:frozen]...)
	used := 0
	for _, i := range res {
		used += m.colSpan(i)
	}
	scroll := pos[frozen:]
	off := m.colOffset
	if off > len(scroll) {
		off = len(scroll)
	}
	for k := off; k < len(scroll); k++ {
		span := m.colSpan(scroll[k])
		if used+span > cw && len(res) > frozen {
			break
		}
		res = append(res, scroll[k])
		used += span
	}
	return res
}

// maxColOffset is the largest colOffset that still fills the view from the
// right edge, so scrolling never reveals empty space past the last column.
func (m Model) maxColOffset() int {
	pos, frozen := m.layoutCols()
	scroll := pos[frozen:]
	cw := m.contentWidth()
	if cw <= 0 || len(scroll) == 0 {
		return 0
	}
	avail := cw
	for _, i := range pos[:frozen] {
		avail -= m.colSpan(i)
	}
	used, fit := 0, 0
	for k := len(scroll) - 1; k >= 0; k-- {
		span := m.colSpan(scroll[k])
		if used+span > avail && fit > 0 {
			break
		}
		used += span
		fit++
	}
	if mo := len(scroll) - fit; mo > 0 {
		return mo
	}
	return 0
}

