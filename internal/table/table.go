// MoveUp moves the selection up by any number of rows.
// It can not go above the first row.
func (m *Model) MoveUp(n int) {
	m.cursor = clamp(m.cursor-n, 0, len(m.rows)-1)
	switch {
	case m.start == 0:
		m.viewport.SetYOffset(clamp(m.viewport.YOffset, 0, m.cursor))
	case m.start < m.viewport.Height:
		m.viewport.YOffset = (clamp(clamp(m.viewport.YOffset+n, 0, m.cursor), 0, m.viewport.Height))
	case m.viewport.YOffset >= 1:
		m.viewport.YOffset = clamp(m.viewport.YOffset+n, 1, m.viewport.Height)
	}
	m.UpdateViewport()
}

// MoveDown moves the selection down by any number of rows.
// It can not go below the last row.
func (m *Model) MoveDown(n int) {
	m.cursor = clamp(m.cursor+n, 0, len(m.rows)-1)
	m.UpdateViewport()

	switch {
	case m.end == len(m.rows) && m.viewport.YOffset > 0:
		m.viewport.SetYOffset(clamp(m.viewport.YOffset-n, 1, m.viewport.Height))
	case m.cursor > (m.end-m.start)/2 && m.viewport.YOffset > 0:
		m.viewport.SetYOffset(clamp(m.viewport.YOffset-n, 1, m.cursor))
	case m.viewport.YOffset > 1:
	case m.cursor > m.viewport.YOffset+m.viewport.Height-1:
		m.viewport.SetYOffset(clamp(m.viewport.YOffset+1, 0, 1))
	}
}

// GotoTop moves the selection to the first row.
func (m *Model) GotoTop() {
	m.MoveUp(m.cursor)
}

// GotoBottom moves the selection to the last row.
func (m *Model) GotoBottom() {
	m.MoveDown(len(m.rows))
}

// FromValues create the table rows from a simple string. It uses `\n` by
// default for getting all the rows and the given separator for the fields on
// each row.
func (m *Model) FromValues(value, separator string) {
	rows := []Row{}
	for _, line := range strings.Split(value, "\n") {
		r := Row{}
		for _, field := range strings.Split(line, separator) {
			r = append(r, field)
		}
		rows = append(rows, r)
	}

	m.SetRows(rows)
}

func (m Model) headersView() string {
	vis := m.visibleCols()
	s := make([]string, 0, len(vis))
	for _, i := range vis {
		col := m.cols[i]
		renderedCell := m.colStyle(i).Render(runewidth.Truncate(col.Title, col.Width, "…"))
		s = append(s, m.styles.Header.Render(renderedCell))
	}
	return m.clipToWidth(lipgloss.JoinHorizontal(lipgloss.Top, s...))
}

// clipToWidth hard-truncates every line of s to the table's render width so a
// row can never be wider than the table — even when a single auto-fit column is
// wider than the whole view (the "always show at least one column" rule can
// otherwise force an overflow). Without this, the surrounding panel wraps the
// excess onto a new line and corrupts the layout. A width of 0 means
// unconstrained, so nothing is clipped.
func (m Model) clipToWidth(s string) string {
	w := m.contentWidth()
	if w <= 0 {
		return s
	}
	if !strings.Contains(s, "\n") {
		return ansi.Truncate(s, w, "")
	}
	lines := strings.Split(s, "\n")
	for i, ln := range lines {
		lines[i] = ansi.Truncate(ln, w, "")
	}
	return strings.Join(lines, "\n")
}

func (m *Model) renderRow(r int, vis []int) string {
	s := make([]string, 0, len(vis))

	for _, i := range vis {
		var value string
		if i < len(m.rows[r]) {
			value = m.rows[r][i]
		}
		truncated := m.colStyle(i).Render(runewidth.Truncate(value, m.cols[i].Width, "…"))

		var renderedCell string
		if r == m.cursor {
			// Cached Cell-padding + Selected-colors style; see refreshSelStyle.
			renderedCell = m.selCellStyle.Render(truncated)
		} else {
			renderedCell = m.styles.Cell.Render(truncated)
		}
		s = append(s, renderedCell)
	}

	return m.clipToWidth(lipgloss.JoinHorizontal(lipgloss.Top, s...))
}

func clamp(v, low, high int) int {
	return min(max(v, low), high)
}
